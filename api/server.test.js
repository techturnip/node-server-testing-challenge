// IMPORTS/INITIALIZATION =========================|
// ================================================|
const request = require('supertest')
const server = require('./server.js')
// ------------------------------------------------|
// TESTING ========================================|
// ================================================|
describe('the server', () => {
  describe('the root endpoint', () => {
    // ASSERTIONS =================================|
    // ============================================|
    // --------------------------------------------|
    it('should return status 200', () => {
      return request(server)
        .get('/')
        .then(res => {
          expect(res.status).toBe(200)
        })
    })
    // --------------------------------------------|
  })
})
